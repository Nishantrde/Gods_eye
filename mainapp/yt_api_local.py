def fetch_top_trending_video(api_key, region_code):
    params = {
        "part": "snippet",
        "chart": "mostPopular",
        "regionCode": region_code,
        "maxResults": 1,  # Fetch only the top video
        "key": api_key,
    }
    response = requests.get(BASE_URL, params=params)
    if response.status_code == 200:
        return response.json()
    else:
        print(f"Failed to fetch for {region_code}: {response.status_code}")
        return None


def main():
    trending_videos = []

    for code, country_name in COUNTRIES.items():
        print(f"Fetching top trending video for {country_name} ({code})...")
        data = fetch_top_trending_video(API_KEY, code)
        if data and "items" in data and len(data["items"]) > 0:
            video = data["items"][0]
            video_info = {
                "country": country_name,
                "title": video["snippet"]["title"],
                "youtube_url": f"https://www.youtube.com/watch?v={video['id']}",
                "thumbnail_url": video["snippet"]["thumbnails"]["high"]["url"],
            }
            trending_videos.append(video_info)

    # Save results to a JSON file
    with open("top_trending_videos.json", "w", encoding="utf-8") as file:
        json.dump(trending_videos, file, indent=4, ensure_ascii=False)

    print("Top trending videos saved to top_trending_videos.json")


if __name__ == "__main__":
    main()
